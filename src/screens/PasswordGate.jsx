export default function PasswordGate({ onUnlock }) {
  const [val,   setVal] = useState('')
  const [err,   setErr] = useState(false)
  const [shake, setShk] = useState(false)
  const [focus, setFocus] = useState(false)

  function attempt() {
    if (val === PWD) { onUnlock(); return }
    setErr(true); setShk(true); setVal('')
    setTimeout(() => setShk(false), 500)
    setTimeout(() => setErr(false), 2000)
  }

  return (
    <div style={{
      width: '100vw', height: '100vh',
      /* Subtle dark gradient matching the reference screenshot */
      background: 'radial-gradient(ellipse at 50% 0%, #1a1f2e 0%, #0d0f14 60%, #0a0b0e 100%)',
      display: 'flex', flexDirection: 'column',
      alignItems: 'center', justifyContent: 'center',
      padding: '0 40px',
      fontFamily: "'Inter', -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif",
    }}>

      {/* Card */}
      <motion.div
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        transition={{ duration: 0.5, ease: [0.32, 0.72, 0, 1] }}
        style={{
          width: '100%', maxWidth: 340,
          display: 'flex', flexDirection: 'column',
          alignItems: 'center',
        }}
      >
        {/* Logo */}
        <motion.div
          initial={{ opacity: 0, y: -8 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ delay: 0.1, duration: 0.5 }}
          style={{ marginBottom: 20, opacity: 0.92 }}
        >
          <TheNormLogo/>
        </motion.div>

        {/* Created by the norm */}
        <motion.div
          initial={{ opacity: 0 }}
          animate={{ opacity: 1 }}
          transition={{ delay: 0.22, duration: 0.5 }}
          style={{
            fontSize: 13,
            fontWeight: 400,
            color: 'rgba(255,255,255,0.35)',
            letterSpacing: '0.01em',
            marginBottom: 44,
          }}
        >Created by the norm</motion.div>

        {/* Form */}
        <motion.div
          animate={shake ? { x: [-8, 8, -6, 6, -3, 3, 0] } : {}}
          transition={{ duration: 0.42 }}
          style={{ width: '100%' }}
        >
          {/* Password label */}
          <div style={{
            fontSize: 11,
            fontWeight: 500,
            color: 'rgba(255,255,255,0.4)',
            letterSpacing: '0.08em',
            textTransform: 'uppercase',
            marginBottom: 8,
          }}>Password</div>

          {/* Input */}
          <div style={{
            background: 'rgba(255,255,255,0.04)',
            border: `1px solid ${err ? 'rgba(220,80,60,0.6)' : focus ? 'rgba(255,255,255,0.2)' : 'rgba(255,255,255,0.08)'}`,
            borderRadius: 8,
            marginBottom: err ? 8 : 16,
            transition: 'border-color 0.18s',
          }}>
            <input
              type="password"
              placeholder="Enter password"
              value={val}
              onChange={e => { setVal(e.target.value); setErr(false) }}
              onKeyDown={e => e.key === 'Enter' && attempt()}
              onFocus={() => setFocus(true)}
              onBlur={() => setFocus(false)}
              autoFocus
              style={{
                width: '100%',
                background: 'none', border: 'none', outline: 'none',
                color: '#fff',
                fontSize: 15,
                fontWeight: 400,
                fontFamily: 'inherit',
                letterSpacing: '0.06em',
                padding: '13px 16px',
              }}
            />
          </div>

          {/* Error */}
          <AnimatePresence>
            {err && (
              <motion.div
                initial={{ opacity: 0, y: -4 }}
                animate={{ opacity: 1, y: 0 }}
                exit={{ opacity: 0 }}
                style={{
                  fontSize: 12,
                  color: 'rgba(220,80,60,0.9)',
                  marginBottom: 10,
                  paddingLeft: 2,
                }}
              >Incorrect password</motion.div>
            )}
          </AnimatePresence>

          {/* Button — white */}
          <motion.button
            onClick={attempt}
            whileHover={{ background: 'rgba(255,255,255,0.92)' }}
            whileTap={{ scale: 0.98, background: 'rgba(255,255,255,0.85)' }}
            style={{
              width: '100%',
              height: 44,
              borderRadius: 8,
              background: '#fff',
              border: 'none',
              fontSize: 14,
              fontWeight: 500,
              fontFamily: 'inherit',
              letterSpacing: '0.02em',
              color: '#0d0f14',
              cursor: 'pointer',
              transition: 'background 0.15s',
            }}
          >Enter</motion.button>
        </motion.div>
      </motion.div>
    </div>
  )
}
